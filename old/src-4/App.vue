<template>
  <div id="app">
    <Title text="todos" />
    <section class="todoapp">

      <header class="header">
        <Checkbox class="toggle-all"
          :value="allDone"
          @change="(value) => { allDone = value }"
        />
        <Textfield
          class="new-todo"
          placeholder="What needs to be done?"
          :value="newTodo"
          @input="(value) => { newTodo = value }"
          @enter="addTodo"
        />
      </header>

      <section class="main" v-show="todos.length" v-cloak>
        <List
          class="todo-list"
          :data="filteredTodos"
        >
          <template slot="item" scope='props'>
            <TodoItem
              :todo="props.item"
              :editedTodo="editedTodo"
              @removeTodo="removeTodo"
              @editTodo="editTodo"
              @doneEdit="doneEdit"
              @cancelEdit="cancelEdit"
            />
          </template>
        </List>
      </section>

      <Nav v-show="todos.length" v-cloak>
        <TodoCount :remaining="remaining"/>
        <List
          class="filters"
          direction="horizontal"
          :data="['all','active','completed']"
          >
          <template slot="item" scope='props'>
            <Button class="filter" @click="setFilter(props.item)" :class="{ selected: visibility == props.item }" >{{props.item}}</Button>
          </template>
        </List>
        <div>
          <Button class="clear-completed" @click="removeCompleted" v-show="todos.length > remaining">Clear completed</Button>
        </div>
      </Nav>
    </section>

    <Info></Info>
  </div>
</template>

<script>

import Title from './components/Title/Title'
import Textfield from './components/Textfield/Textfield'
import Info from './components/Info/Info'
import TodoCount from './components/TodoCount/TodoCount'
import Button from './components/Button/Button'
import Label from './components/Label/Label'
import Checkbox from './components/Checkbox/Checkbox'
import List from './components/List/List'
import Nav from './components/Nav/Nav'
import TodoItem from './components/TodoItem/TodoItem'
import './App.css'

export default {
  name: 'app',
  components: { Title, Info, TodoCount, Button, Textfield, Label, Checkbox, List, TodoItem, Nav },
  data: function () {
    return {
      todos: todoStorage.fetch(),
      newTodo: '',
      editedTodo: null,
      visibility: 'active'
    }
  },
  watch: {
    todos: {
      handler: function (todos) {
        todoStorage.save(todos)
      },
      deep: true
    }
  },

  // computed properties
  // http://vuejs.org/guide/computed.html
  computed: {
    filteredTodos: function () {
      return filters[this.visibility](this.todos)
    },
    remaining: function () {
      return filters.active(this.todos).length
    },
    allDone: {
      get: function () {
        return this.remaining === 0
      },
      set: function (value) {
        this.todos.forEach(function (todo) {
          todo.completed = value
        })
      }
    }
  },

  filters: {
    pluralize: function (n) {
      return n === 1 ? 'item' : 'items'
    }
  },

  // methods that implement data logic.
  // note there's no DOM manipulation here at all.
  methods: {
    addTodo: function () {
      var value = this.newTodo && this.newTodo.trim()
      if (!value) {
        return
      }
      this.todos.push({
        id: todoStorage.uid++,
        title: value,
        completed: false
      })
      this.newTodo = ''
    },

    removeTodo: function (todo) {
      this.todos.splice(this.todos.indexOf(todo), 1)
    },

    editTodo: function (todo) {
      this.beforeEditCache = todo.title
      this.editedTodo = todo
    },

    doneEdit: function (todo) {
      if (!this.editedTodo) {
        return
      }
      this.editedTodo = null
      todo.title = todo.title.trim()
      if (!todo.title) {
        this.removeTodo(todo)
      }
    },

    cancelEdit: function (todo) {
      this.editedTodo = null
      todo.title = this.beforeEditCache
    },

    removeCompleted: function () {
      this.todos = filters.active(this.todos)
    },

    setFilter: function (filter) {
      this.visibility = filter
    }
  },

  // a custom directive to wait for the DOM to be updated
  // before focusing on the input field.
  // http://vuejs.org/guide/custom-directive.html
  directives: {
    'todo-focus': function (el, binding) {
      if (binding.value) {
        el.focus()
      }
    }
  }
}

// localStorage persistence
var STORAGE_KEY = 'todos-vuejs-2.0'
var todoStorage = {
  fetch: function () {
    var todos = JSON.parse(localStorage.getItem(STORAGE_KEY) || '[]')
    todos.forEach(function (todo, index) {
      todo.id = index
    })
    todoStorage.uid = todos.length
    return todos
  },
  save: function (todos) {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(todos))
  }
}

// visibility filters
var filters = {
  all: function (todos) {
    return todos
  },
  active: function (todos) {
    return todos.filter(function (todo) {
      return !todo.completed
    })
  },
  completed: function (todos) {
    return todos.filter(function (todo) {
      return todo.completed
    })
  }
}
</script>

